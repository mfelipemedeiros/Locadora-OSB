    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        
        try{
        
            Usuario usuario = new Usuario();
            usuario.setNome(jTextFieldNome.getText());
            usuario.setCpf(jTextFieldCPF.getText());
            usuario.setUsuario(jTextFieldUsuario.getText());
            usuario.setSenha(jTextFieldSenha.getText());

            UsuarioDAO usuarioDAO = new UsuarioDAO();
            usuarioDAO.cadastrar(usuario);
            JOptionPane.showMessageDialog(this, "Cadastrado com sucesso!", "Aviso!!", 1);
            limparCampos();
        }
        catch(Exception e){
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Entre com o valor correto!!", "AVISO", 1);
            jTextFieldCPF.setText("");
            jTextFieldCPF.requestFocus();
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

